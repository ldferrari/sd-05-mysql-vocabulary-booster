SELECT j.JOB_TITLE AS 'Cargo',
CASE
WHEN ROUND(AVG(e.SALARY),2) <= 5800 THEN 'JÃºnior'
WHEN ROUND(AVG(e.SALARY),2) >= 5801 AND ROUND(AVG(e.SALARY),2) <= 7500 THEN 'Pleno'
WHEN ROUND(AVG(e.SALARY),2) >= 7501 AND ROUND(AVG(e.SALARY),2) <= 10500 THEN 'Senior'
WHEN ROUND(AVG(e.SALARY),2) > 10500 THEN 'CEO'
END AS 'Senioridade'
FROM hr.jobs AS j
INNER JOIN hr.employees AS e ON
j.JOB_ID = e.JOB_ID
GROUP BY j.JOB_TITLE
ORDER BY ROUND(AVG(e.SALARY),2), Cargo;
